package pt.lsts.neptus.systems.links;

import java.net.InetAddress;

import pt.lsts.neptus.systems.SystemsManager.SystemLinkType;

/**
 * @author Hugo
 * 
 */
public abstract class SystemLink {
    private SystemLinkType type;
    private InetAddress ip;
    private int port;

    public SystemLink() {
    }

    /**
     * @return the type
     */
    public SystemLinkType getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(SystemLinkType type) {
        this.type = type;
    }

    /**
     * @return the ip
     */
    public InetAddress getIp() {
        return ip;
    }

    /**
     * @param ip the ip to set
     */
    public void setIp(InetAddress ip) {
        this.ip = ip;
    }

    /**
     * @return the port
     */
    public int getPort() {
        return port;
    }

    /**
     * @param port the port to set
     */
    public void setPort(int port) {
        this.port = port;
    }
    
    
}
